/**
 * بدء نظام التذكير المجدول التلقائي للمجموعات
 * @param {Object} sock - كائن اتصال الواتساب
 */
export function startAdhkarAutomation(sock) {
    // تشغيل التذكير كل 30 دقيقة (1800000 مللي ثانية)
    setInterval(async () => {
        try {
            // نستخدم استعلام مباشر على SQLite لجلب المجموعات المفعّل فيها الأذكار
            const { db } = await import('./db.js');
            const enabledGroups = db.prepare('SELECT jid FROM groups WHERE autoAdhkar = 1').all();

            for (const { jid } of enabledGroups) {
                try {
                    const randomDhikr = adhkarList[Math.floor(Math.random() * adhkarList.length)];
                    const message = `✨ *تذكير بالذكر اليومي* ✨\n\n${randomDhikr}\n\n🌸 شاركها لتعم الأجر والمغفرة.`;
                    await sock.sendMessage(jid, { text: message });
                } catch (groupErr) {
                    logger.error(`فشل إرسال الأذكار للمجموعة ${jid}:`, groupErr.message);
                }
            }
        } catch (e) {
            logger.error('خطأ في تشغيل نظام الأذكار التلقائي:', e);
        }
    }, 1800000);
}

