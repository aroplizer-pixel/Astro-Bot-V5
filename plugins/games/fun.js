// 💖 مقياس التوافق بين شخصين
registerCommand('توافق', async (ctx) => {
    let target = null;
    let targetName = '';

    // استخراج الشخص الآخر من المنشن أو الاقتباس أو النص
    if (ctx.msg.message?.extendedTextMessage?.contextInfo?.mentionedJid?.length > 0) {
        target = ctx.msg.message.extendedTextMessage.contextInfo.mentionedJid[0];
        targetName = `@${target.split('@')[0]}`;
    } else if (ctx.msg.message?.extendedTextMessage?.contextInfo?.participant) {
        target = ctx.msg.message.extendedTextMessage.contextInfo.participant;
        targetName = `@${target.split('@')[0]}`;
    } else if (ctx.args.length > 0) {
        targetName = ctx.args.join(' ');
    }

    if (!targetName) {
        return ctx.reply('❌ يرجى منشن الشخص أو كتابة اسمه لقياس نسبة التوافق معه!\nمثال: *.توافق @منشن*');
    }

    const percentage = Math.floor(Math.random() * 101);
    let comment = '';
    let emoji = '❤️';

    if (percentage >= 90) {
        comment = "💖 حب حقيقي وعلاقة أسطورية مكتوب لها النجاح التام! 🔥";
        emoji = "👑💖";
    } else if (percentage >= 70) {
        comment = "💞 توافق كبير وتفاهم رائع جداً، مستمرون بقوة! ✨";
        emoji = "🥰💕";
    } else if (percentage >= 50) {
        comment = "🤝 علاقة طيبة كأصدقاء مقربين ورفقاء درب رائعين.";
        emoji = "🌸😊";
    } else if (percentage >= 30) {
        comment = "⚠️ هناك بعض الاختلافات الكبيرة والمشاحنات الطفيفة، حاولا التفاهم أكثر!";
        emoji = "🤷‍♂️⚡";
    } else {
        comment = "💔 توافق ضعيف جداً وشبه مستحيل! يرجى الحذر والابتعاد الفوري لسلامتكم! 😂";
        emoji = "☠️💔";
    }

    let text = `💖 *مقياس التوافق والانسجام العاطفي* 💖\n`;
    text += `━━━━━━━━━━━━━━━━━━━━━\n\n`;
    text += `👤 *الطرف الأول:* @${ctx.senderNumber}\n`;
    text += `👤 *الطرف الثاني:* ${targetName}\n\n`;
    text += `📈 *نسبة التوافق:* [ *${percentage}%* ] ${emoji}\n\n`;
    text += `📝 *التحليل:* ${comment}\n\n`;
    text += `━━━━━━━━━━━━━━━━━━━━━`;

    const mentions = [ctx.sender];
    if (target) mentions.push(cleanJid(target));

    await ctx.sock.sendMessage(ctx.from, { text, mentions }, { quoted: ctx.msg });
}, {
    description: 'قياس نسبة التوافق والانسجام بينك وبين شخص آخر بالمنشن أو الاسم',
    category: '🎮 ألعاب وتسلية'
});

